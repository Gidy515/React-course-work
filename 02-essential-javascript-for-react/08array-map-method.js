// the map method: this method creates a new array of the same length as the original array, but with results of some operation performed on each element of the original array.

// quick example:
let arr = [1, 2, 3, 4, 5];
const x = arr.map((el) => el * 2); // this will log a new array with each element of the original array multiplied by 2. The output will be [2, 4, 6, 8, 10].
console.log(x);

// let's say we want to create a new array of book titles from the books array. We can use the map method to do this:
